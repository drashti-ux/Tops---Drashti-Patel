//Write a program to demonstrate the use of different data types.
package datatypes_variables_operators;

public class Lab_Q1 {
	public static void main(String []args) {
		byte num = 5;
		short num1 = 127;
		int num2 =547548652;
		long num3 = 35944842346l;
		float num4 = 10.9f;
		double num5 = 2584645214.025;
		char ch = 'a';
		System.out.println("Byte Number:"+num);
		System.out.println("Short Number : "+num1);
		System.out.println("Integer Number : "+num2);
		System.out.println("Long Number : "+num3);
		System.out.println("float Number : "+num4);
		System.out.println("Double Number : "+num5);
		System.out.println("Character : "+ch);
	}
}
